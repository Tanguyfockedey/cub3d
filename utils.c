#include "cub3d.h"

void	print_map(t_map *map)
{
	int	i;
	int	j;

	i = 0;
	while (i < map->height)
	{
		j = 0;
		while (j < map->width)
		{
			printf("%c ", map->tiles[i * map->width + j]);
			j++;
		}
		printf("\n");
		i++;
	}
}
